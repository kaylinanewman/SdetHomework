package org.example.e15;

public class E15TimeConversionModulus {
    public static void main(String[] args) {

        int totalMinutes = 125;
        int hours = 125/60;
        int minutes = 125%60;

        System.out.println(totalMinutes + " minutes is equal to " + hours + " hours" + " and " + minutes + " minutes.");



    }
}
